// Half Triangle
class halfTri {
    public static void main(String argus[]) {
        // loop for the row
        for (int l = 1; l <= 5; l++) {
            // loop for coloumn
            for (int i = 1; i <= l; i++) {
                System.out.print("+");
            }
            System.out.println();
        }
    }
}
